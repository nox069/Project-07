  get_pressure_temperature(&sensor, OSR_4096);
  pressure_i = sensor.pressure;
  HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, SET);
  HAL_Delay(100);

  EKF_Init(&ekf);

  altitude_kf_init(&alt_kf, 0.0f, pressure_i, 20.0f);  // Start at 0m, sea level pressure 1013.25 hPa, 20Â°C
